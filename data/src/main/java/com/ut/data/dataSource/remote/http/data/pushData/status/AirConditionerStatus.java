package com.ut.data.dataSource.remote.http.data.pushData.status;

public class AirConditionerStatus {


    private String indoorTem;

    private String tem;

    private String status;

    private String mode;

    private String wind;

    private String swingLR;

    private String swingUD;

    private String sleep;


    public String getIndoorTem() {
        return indoorTem;
    }

    public void setIndoorTem(String indoorTem) {
        this.indoorTem = indoorTem;
    }

    public String getTem() {
        return tem;
    }

    public void setTem(String tem) {
        this.tem = tem;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getMode() {
        return mode;
    }

    public void setMode(String mode) {
        this.mode = mode;
    }

    public String getWind() {
        return wind;
    }

    public void setWind(String wind) {
        this.wind = wind;
    }

    public String getSwingLR() {
        return swingLR;
    }

    public void setSwingLR(String swingLR) {
        this.swingLR = swingLR;
    }

    public String getSwingUD() {
        return swingUD;
    }

    public void setSwingUD(String swingUD) {
        this.swingUD = swingUD;
    }

    public String getSleep() {
        return sleep;
    }

    public void setSleep(String sleep) {
        this.sleep = sleep;
    }
}
